#include "libft.h"

int		ft_isokay(char c)
{
	if (c == 'z' || c == 'h' || c == 'l' || c == '+' || c == '-' || c == '#'
		|| c == ' ' || c == '*' || c == '.' || c == 'j' || ft_isdigit(c))
		return (1);
	return (0);
}

int		ft_fuckit(char c)
{
	if (c == 'o' || c == 'O' || c == 'x' || c == 'X' || c == 'p'
		|| c == 'd' || c == 'D' || c == 'i' || c == 'c' || c == 'C')
		return (1);
	return (0);
}
